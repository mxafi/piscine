#include <unistd.h>
#include <fcntl.h>

void	display_file(char *filename)
{
	int		file;
	char	c;

	file = open(filename, O_RDONLY);
	while ((read(file, &c, sizeof c)) > 0)
	{
		write(1, &c, 1);
	}
	close(file);
}

int	main(int argc, char **argv)
{
	if (argc < 2)
		write(2, "File name missing.\n", 19);
	else if (argc > 2)
		write(2, "Too many arguments.\n", 20);
	else
		display_file(argv[1]);
	return (0);
}
